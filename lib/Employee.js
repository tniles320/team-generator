class Employee {
    constructor(name, id, email) {
        this.name = name;
        this.id = id;
        this.email = email;
    }
    getName() {
        return this.name;
    }
    getId() {
        return this.id;
    }
    getEmail() {
        return this.email;
    }
    getRole() {
        return "Employee"
    }
}

const Alice = new Employee("Alice", 100, "test@test.com");

Alice.getName();
Alice.getId();
Alice.getEmail();


module.exports = Employee;
